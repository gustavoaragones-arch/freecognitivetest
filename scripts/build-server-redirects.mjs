#!/usr/bin/env node
/**
 * Emit server-level 301 redirects (_redirects, vercel.json, .htaccess).
 * No client-side redirects — pair with removal of applyClientRedirects in common.js.
 */
import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

/** ARCH-01 Part 3: test legacy → canonical /tests/* (server only). */
const TEST_LEGACY_REDIRECTS = [
  { from: "/mini-cog-test/", to: "/tests/mini-cog-test.html" },
  { from: "/mini-cog-test", to: "/tests/mini-cog-test.html" },
  { from: "/clock-drawing-test/", to: "/tests/clock-drawing-test.html" },
  { from: "/clock-drawing-test", to: "/tests/clock-drawing-test.html" },
  { from: "/es/prueba-mini-cog/", to: "/es/tests/mini-cog-test.html" },
  { from: "/es/prueba-mini-cog", to: "/es/tests/mini-cog-test.html" },
  { from: "/es/test-reloj/", to: "/es/tests/clock-drawing-test.html" },
  { from: "/es/test-reloj", to: "/es/tests/clock-drawing-test.html" },
  { from: "/es/test-dibujo-reloj/", to: "/es/tests/clock-drawing-test.html" },
  { from: "/es/test-dibujo-reloj", to: "/es/tests/clock-drawing-test.html" },
  { from: "/fr/test-mini-cog/", to: "/fr/tests/mini-cog-test.html" },
  { from: "/fr/test-mini-cog", to: "/fr/tests/mini-cog-test.html" },
  { from: "/fr/test-horloge-dessin/", to: "/fr/tests/clock-drawing-test.html" },
  { from: "/fr/test-horloge-dessin", to: "/fr/tests/clock-drawing-test.html" },
];

const legalRedirects = JSON.parse(readFileSync(join(ROOT, "redirects.json"), "utf8"));
const allRules = [...TEST_LEGACY_REDIRECTS, ...legalRedirects];

const seen = new Set();
const rules = [];
for (const r of allRules) {
  const key = `${r.from}\0${r.to}`;
  if (seen.has(key)) continue;
  seen.add(key);
  rules.push(r);
}

const redirectsLines = rules.map((r) => `${r.from} ${r.to} 301`).join("\n");
writeFileSync(join(ROOT, "_redirects"), `${redirectsLines}\n`, "utf8");

const vercelRedirects = rules.map((r) => ({
  source: r.from,
  destination: r.to,
  permanent: true,
}));
writeFileSync(
  join(ROOT, "vercel.json"),
  `${JSON.stringify({ redirects: vercelRedirects }, null, 2)}\n`,
  "utf8"
);

const htaccess = [
  "# ARCH-01 server redirects — generated by scripts/build-server-redirects.mjs",
  "RewriteEngine On",
  ...rules.map((r) => {
    const from = r.from.replace(/\/$/, "") || "/";
    return `RedirectMatch 301 ^${escapeRegex(from)}/?$ ${r.to}`;
  }),
  "",
].join("\n");
writeFileSync(join(ROOT, ".htaccess"), htaccess, "utf8");

writeFileSync(join(ROOT, "redirects.json"), `${JSON.stringify(rules, null, 2)}\n`, "utf8");

console.log(`Wrote ${rules.length} server redirect rules → _redirects, vercel.json, .htaccess, redirects.json`);

function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
